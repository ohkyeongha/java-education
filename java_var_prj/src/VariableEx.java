
public class VariableEx {

	public static void main(String[] args) {
		/*
		 * VariableEx
		 * 본인 소개
		 * - 이름 :
		 * - 나이 : 
		 * - 키 : 
		 * - 학교 :
		 * - 주소 :
		 * - 핸드폰번호 :
		 * - 그 외 내용:
		 * 
		 */
		
		String name = "Luxy";
		int age = 25;
		int higth = 163;
		String cal = "가갸대학교";
		String addr = "서울";
		String ph_num = "010-79**-****";
		String etc = "잘 부탁드립니다";
		
		System.out.println("이름 \t: " + name);
		System.out.println("나이 \t: " + age);
		System.out.println("키 \t: " + higth);
		System.out.println("학교 \t: " + cal);
		System.out.println("주소 \t: " + addr);
		System.out.println("번호 \t: " + ph_num);
		System.out.println(etc);
		

	}

}
